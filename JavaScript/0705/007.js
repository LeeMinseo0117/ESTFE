// 변수 선언
// 변수 키워드 - var, let, const

var a;

let b = 10; // 선언 + 할당 (초기화)

// const는 선언과 동시에 할당
const name = "weniv";

// 변수 저장
a = 30;
b = 20;

// const로 선언한 변수는 재할당이 불가능함
// 따라서 오류가 남
// name = 'hello';

// 변수 참조
console.log(a); //a 변수 값을 가져옴
console.log(b); //b 변수 값을 가져옴
console.log(name); //name 변수 값을 가져옴
